nota = int(input("Olá estudante, por favor insira a pontuação e mostrarei a nota final:"))
if (nota>=90):
    print(f"Parabéns! sua nota é 'A',continue assim!")
elif (nota>=80):
    print (f"fantástico! sua nota é 'B',continue assim e chegue no topo!")
elif (nota>=70):
    print (f"sua nota é 'C',não desista!")
elif (nota>=60):
    print (f"Poxa! sua nota total é 'D',não se sinta mal!")
else:
    print(f"sua nota total é 'F',você ainda tem um potencial incrivel pela frente! respire!")
